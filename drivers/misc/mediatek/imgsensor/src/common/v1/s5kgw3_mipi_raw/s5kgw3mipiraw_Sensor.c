extern int imgsensor_get_cal_buf_by_sensor_idx(int sensor_idx, char **buf);


#define SENSOR_GW3_GGC_CAL_BASE_REAR           (0x2A96)
#define SENSOR_GW3_GGC_CAL_SIZE                (92)
#define SENSOR_GW3_GGC_REG_ADDR                (0x39DA)

static void sensor_GGC_write(void)
{
	int i = 0;
	char *rom_cal_buf = NULL;
	char cal_data[SENSOR_GW3_GGC_CAL_SIZE - 2] = {0};

	int start_addr = SENSOR_GW3_GGC_REG_ADDR;
	int data_size = SENSOR_GW3_GGC_CAL_SIZE - 2;

	// TODO : sensor_dev_id needs to be updated according to actual id.
	int sensor_dev_id = IMGSENSOR_SENSOR_IDX_MAIN;

	pr_debug("%s", __func__);

	if (imgsensor_get_cal_buf_by_sensor_idx(sensor_dev_id, &rom_cal_buf)) {
		pr_debug("%s : cal data is NULL", __func__);
		return;
	}
	rom_cal_buf += SENSOR_GW3_GGC_CAL_BASE_REAR;
	if (rom_cal_buf[0] == 0xFF && rom_cal_buf[1] == 0x00) {
		// Big Endian
		memcpy(cal_data, &rom_cal_buf[2], SENSOR_GW3_GGC_CAL_SIZE - 2);
	} else if (rom_cal_buf[0] == 0x00 && rom_cal_buf[1] == 0xFF) {
		// Little Endian
		rom_cal_buf += 2;
		for (i = 0; i + 1 < SENSOR_GW3_GGC_CAL_SIZE - 2; i += 2) {
			cal_data[i] = rom_cal_buf[i+1];
			cal_data[i+1] = rom_cal_buf[i];
		}
		if ((SENSOR_GW3_GGC_CAL_SIZE-2) & 0x1)
			cal_data[i] = rom_cal_buf[i];
	} else {
		pr_debug("%s : endian check fail (%X, %X)",
			__func__, rom_cal_buf[0], rom_cal_buf[1]);
		return;
	}

	write_cmos_sensor(0x6028, 0x2000);
	write_cmos_sensor(0x602A, start_addr);
#ifdef data_size_is_even
	//data size is even
	for (i = 0; i < data_size / 2; i++)
		write_cmos_sensor(0x6F12, cal_data[2 * i]);
#else
	for (i = 0; i < data_size; i++)
		write_cmos_sensor_8(0x6F12, cal_data[i]);
#endif
	pr_info("%s done writing %d bytes", __func__, data_size);
}
